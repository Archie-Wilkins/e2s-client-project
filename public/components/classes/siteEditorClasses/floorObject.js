class FloorObject{
    constructor(id, name, image, width, height, x, y, z, rotation, floor, type, isLocked, isHidden){
        this.id = id;
        this.name = name;
        this.image = image;
        this.width = width;
        this.height = height;
        this.x = x;
        this.y = y;
        this.z = z;
        this.rotation = rotation;
        this.floor = floor;
        this.type = type;
        this.isLocked = isLocked;
        this.isHidden = isHidden;
    }
}